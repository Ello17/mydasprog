# Menghitung jumlah dari semua angka dalam sebuah list
# Daftar angka
numbers = [7, 5, 9, 8, 9, 0, 8, 4, 0]

# Inisialisasi variabel penampung jumlah
sum = 0

# Looping untuk menjumlahkan setiap angka dalam list
for each in numbers :
    sum = sum + each
    
# Menampilkan hasil jumlah
print("Jumlah dari semua angka adalah : "+str(sum))


